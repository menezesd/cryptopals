# Encrypt all plaintexts with the same key and nonce
ciphertexts = [aes_ctr(base64.b64decode(p), KEY, nonce=0) for p in PLAINTEXTS_B64]

# Since all ciphertexts share the same keystream, we can recover it
# by scoring each keystream byte position against all ciphertexts.
max_len = max(len(c) for c in ciphertexts)


def score_byte(b):
    """Score a byte as likely English plaintext."""
    if chr(b) in "etaoinshrdlu ETAOINSHRDLU":
        return 3
    if chr(b).isalpha() or b == ord(",") or b == ord(".") or b == ord("'") or b == ord(";"):
        return 1
    if chr(b).isprintable():
        return 0
    return -10


keystream = bytearray(max_len)
for pos in range(max_len):
    best_score = -999999
    best_byte = 0
    for guess in range(256):
        score = 0
        for ct in ciphertexts:
            if pos < len(ct):
                plaintext_byte = ct[pos] ^ guess
                score += score_byte(plaintext_byte)
        if score > best_score:
            best_score = score
            best_byte = guess
    keystream[pos] = best_byte

# Decrypt and display
for ct in ciphertexts:
    decrypted = xor_bytes(ct, keystream[:len(ct)])
    print(decrypted.decode("ascii", errors="replace"))
